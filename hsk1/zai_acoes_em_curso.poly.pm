在 (zài) e 正在 (zhèngzài) podem ser utilizados como ◊ref{verbos auxiliares} para sinalizar que uma ação está em curso ou em progresso. Isso muitas vezes equivale ao *presente contínuo* em português.

Você pode utilizar 正在 (zhèngzài) ao invés de apenas 在 (zài) para enfatizar um pouco mais que a ação está acontecendo **agora mesmo**.

◊section{Estrutura}

◊grammarstruct{Suj. + 在 + Verbo + Obj.}

ou

◊grammarstruct{Suj. + 正在 + Verbo + Obj.}

