#lang pollen

◊(define-meta title "Expressando ações em curso com 在")
◊(define-meta id "zai-hsk1")
◊(define-meta tags ('gramatica 'hsk1 'a2 'adverbios))

在 (zài) e 正在 (zhèngzài) podem ser utilizados como ◊ref{verbos auxiliares} para sinalizar que uma ação está em curso ou em progresso. Isso muitas vezes equivale ao *presente contínuo* em português.

Você pode utilizar 正在 (zhèngzài) ao invés de apenas 在 (zài) para enfatizar um pouco mais que a ação está acontecendo **agora mesmo**.

◊section{Estrutura}

◊grammarstruct{Suj. + 在 + Verbo + Obj.}

ou

◊grammarstruct{Suj. + 正在 + Verbo + Obj.}

◊section{Exemplos}

◊example{ ◊zh{她在看书。}
          ◊py{Tā zài kànshū.}
          ◊pt{Ela está lendo.}
        }

◊example{ ◊zh{妈妈在打电话。}
          ◊py{Māma zài dǎ diànhuà.}
          ◊pt{Mamãe está falando ao telefone.}
        }

◊example{ ◊zh{谁在里面洗澡。}
          ◊py{Shéi zài lǐmiàn xǐzǎo?}
          ◊pt{Quem está tomando banho ali?}
        }

◊example{ ◊zh{阿姨正在打扫我们的房间。}
          ◊py{Āyí zhèngzài dǎsǎo wǒmen de fángjiān.}
          ◊pt{A faxineira está limpando o nosso quarto neste momento.}
        }

◊example{ ◊zh{昨天晚上七点，我们在吃饭。}
          ◊py{Zuótiān wǎnshang qīdiǎn, wǒmen zài chīfàn.}
          ◊pt{Ontem às 7 da noite estávamos jantando.}
        }

◊example{ ◊zh{老板在开会，没有时间你。}
          ◊py{Lǎobǎn zài kāihuì, méiyǒu shíjiān jiàn nǐ.}
          ◊pt{O chefe está em reunião, não tem tempo para ver você agora.}
        }

◊example{ ◊zh{我现在在上班，不方便离开。}
          ◊py{Wǒ xiànzài zài shàngbān, bù fāngbiàn líkāi.}
          ◊pt{Estou trabalhando agora, fica difícil sair.}
        }

◊example{ ◊zh{我们正在上课，请你等一会儿。}
          ◊py{Wǒmen zhèngzài shàngkè, qǐng nǐ děng yīhuìr.}
          ◊pt{Estamos em aula agora, espere um pouco, por favor.}
        }

◊example{ ◊zh{你正在开车，不可以玩手机。}
          ◊py{Nǐ zhèngzài kāichē, bù kěyǐ wán shǒujī.}
          ◊pt{Você está dirigindo neste momento, não pode usar o celular.}
        }

◊example{ ◊zh{你给我打电话的时候，我正在跟朋友打游戏。}
          ◊py{Nǐ gěi wǒ dǎ diànhuà de shíhou, wǒ zhèngzài gēn péngyou dǎ yóuxì.}
          ◊pt{Na hora que você ligou eu estava jogando videogame com os amigos.}
        }

